package e0123_TestApiStream_4_GroupingBy;

public class Worker23 {
    private String name;
    private int age;
    private int salary;
    private String position;

    public Worker23(String name, int age, int salary, String position) {
        this.name = name;
        this.age = age;
        this.salary = salary;
        this.position = position;
    }

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }

    public int getSalary() {
        return salary;
    }

    public String getPosition() {
        return position;
    }
}
